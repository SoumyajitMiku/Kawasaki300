package com.ArrayPractice;

public class PolymorphismByNoOfParameters {
	

	public void noOfParameters()
	{
		System.out.println("method without any argument");
	}
	public void noOfParameters(int x)
	{
		System.out.println("method has one argument");
	
		
	}
	public void noOfParameters(int x,int y)
	{
		System.out.println("method has two arguments");
	}




		
	
}
